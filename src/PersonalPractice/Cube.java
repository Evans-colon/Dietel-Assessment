package PersonalPractice;

import java.util.Scanner;

public class Cube {
    public static void main(String[] args) {

        int count = 0;

        for (count = 1; count <= 10; count++) {
            System.out.println("number is: " + count + " and the cube of " + count + " is: " + (count * count * count));

        }

    }

}
